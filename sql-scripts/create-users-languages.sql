USE CODE_HELP;

DROP TABLE IF EXISTS CH_LANGUAGES;

CREATE TABLE `CH_LANGUAGES` (
    `LANGUAGE_ID` INT NOT NULL AUTO_INCREMENT,
    `USER_ID` VARCHAR(200) NOT NULL,    
    `LANGUAGE_NAME` VARCHAR(150) NOT NULL,
    PRIMARY KEY(LANGUAGE_ID),
    UNIQUE(USER_ID,LANGUAGE_NAME),
    FOREIGN KEY(USER_ID) REFERENCES CH_USERS(USER_ID)
);

